package poluchChisla;

public class Znak {
    public String znak;

    public String getZnak() {
        return znak;
    }

    public void setZnak(String znak) {
        this.znak = znak;
    }

    @Override
    public String toString() {
        return "Znak{" +
                "znak='" + znak + '\'' +
                '}';
    }
}
